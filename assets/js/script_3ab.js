function pintar1(){
    const caja_1= document.getElementById("caja1")  
    if(caja_1.style.backgroundColor=="black"){
        caja_1.style.backgroundColor = 'blue'
    }
    else{
        caja_1.style.backgroundColor = 'black'
    }   
}
